class ADITYA:
    @ staticmethod
    def add(x,y):
        print("The sum value is:",(x+y))
    @ staticmethod
    def sub(x,y):
        print("The subtracted value is:",(x-y))
    @ staticmethod
    def avg(x,y):
        print("The average value is:",(x+y)/2)
ADITYA.add(700000000,500000)
ADITYA.sub(9000000,500000)
ADITYA.avg(7000,500000)
    
    
